#include "libft.h"

int	ft_atoi(const char *str)
{
	long long	ch;
	int			p;
	int			i;

	while ((*(str) >= 9 && *(str) <= 13) || *(str) == 32)
		str++;
	p = (*(str) == '-') ? -1 : 1;
	str = (*(str) == '-' || *(str) == '+') ? str + 1 : str;
	ch = 0;
	i = 0;
	while (*(str) >= '0' && *(str) <= '9')
	{
		if (++i == 20 || (ch >= 922337203685477580 && ch * 10 + (*(str) - '0')
			< 922337203685477580))
			return (p < 0 ? 0 : -1);
		ch = ch * 10 + (*(str++) - '0');
	}
	return ((int)ch * p);
}
