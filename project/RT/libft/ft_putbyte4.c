#include "libft.h"

void	ft_putbyte4(int *b)
{
	int	size;

	if (b)
	{
		size = 32;
		while (--size >= 0)
		{
			write(1, (*b & (1 << size)) ? "1" : "0", 1);
			if (!(size % 8))
				write(1, " ", 1);
		}
		write(1, "\n", 1);
	}
	else
		write(2, "error\n", 6);
}
