#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *str;

	str = &s1[ft_strlen(s1)];
	while (*s2 && n--)
		*(str++) = *(s2++);
	*(str) = '\0';
	return (s1);
}
