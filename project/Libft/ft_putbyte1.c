#include "libft.h"

void	ft_putbyte1(unsigned char *b)
{
	int	size;

	if (b)
	{
		size = 8;
		while (--size >= 0)
			write(1, (*b & (1 << size)) ? "1" : "0", 1);
		write(1, "\n", 1);
	}
	else
		write(2, "error\n", 6);
}
