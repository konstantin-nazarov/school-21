#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	unsigned char	*str1;
	unsigned char	*str2;

	str1 = (unsigned char *)dst;
	str2 = (unsigned char *)src;
	while (n--)
	{
		*str1++ = *str2++;
		if (*(str2 - 1) == (unsigned char)c)
			return ((void *)str1);
	}
	return (0);
}
