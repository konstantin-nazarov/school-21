#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (n == 0)
		return (1);
	while (s1 && s2 && i < n && (s1[i] == s2[i]))
	{
		if (i == n - 1 || (s1[i] == '\0' && s2[i] == '\0'))
			return (1);
		i++;
	}
	return (0);
}
