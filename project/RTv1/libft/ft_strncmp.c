#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t		i;

	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] != s2[i] && s1[i] >= 0 && s2[i] >= 0 && i < n)
			return (s1[i] - s2[i]);
		if ((s1[i] < -127 || s2[i] < -127) && i < n)
			return (1);
		i++;
	}
	return (0);
}
